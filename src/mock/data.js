const ordered = () => {
    const curated = {};
    data.forEach(({ date, gross, net, volume }) => {
        if (!curated.hasOwnProperty(date)) {
            curated[date] = { date, gross, net, volume };
            return;
        }
        curated[date].gross = curated[date].gross + gross;
        curated[date].net = curated[date].net + net;
        curated[date].volume = curated[date].volume + volume;
        return;
    });
    return Object.values(curated).sort(
        (a, b) => new Date(b.date) - new Date(a.date)
    );
};

const victoryData = () => {
    const result = {
        curated: [[], [], []],
        maxima: 0,
        cards: {},
    };
    ordered().forEach((i) => {
        result.curated[0].push({
            x: i.date,
            y: i.gross.toFixed(1),
            l: "gross",
        });
        result.curated[1].push({ x: i.date, y: i.net.toFixed(1), l: "net" });
        result.curated[2].push({
            x: i.date,
            y: i.volume.toFixed(1),
            l: "transaction",
        });
    });
    data.forEach((i) => {
        if (!result.cards.hasOwnProperty(i.payment)) {
            result.cards[i.payment] = 1;
        } else {
            result.cards[i.payment] = result.cards[i.payment] + 1;
        }
    });
    result.curated[0] = result.curated[0].slice(0, 15);
    result.curated[1] = result.curated[1].slice(0, 15);
    result.curated[2] = result.curated[2].slice(0, 15);
    result.maxima = result.curated.map((dataset) =>
        Math.max(...dataset.map((d) => d.y)).toFixed(1)
    );
    return result;
};

